static HAL_StatusTypeDef UART_WaitOnFlagUntilTimeout(UART_HandleTypeDef *huart, uint32_t Flag, FlagStatus Status, uint32_t Tickstart, uint32_t Timeout)
{
  /* Wait until flag is set */
  while((__HAL_UART_GET_FLAG(huart, Flag) ? SET : RESET) == Status) 
  {
    /* Check for the Timeout */
    if(Timeout != HAL_MAX_DELAY)
    {
      if((Timeout == 0U)||((HAL_GetTick() - Tickstart ) > Timeout))
      {
        /* Disable TXE, RXNE, PE and ERR (Frame error, noise error, overrun error) interrupts for the interrupt process */
        CLEAR_BIT(huart->Instance->CR1, (USART_CR1_RXNEIE | USART_CR1_PEIE | USART_CR1_TXEIE));
        CLEAR_BIT(huart->Instance->CR3, USART_CR3_EIE);
        
        huart->gState  = HAL_UART_STATE_READY;
        huart->RxState = HAL_UART_STATE_READY;
        
        /* Process Unlocked */
        __HAL_UNLOCK(huart);
        
        return HAL_TIMEOUT;
      }
    }
  }
  
  return HAL_OK;
}

void MainScreenView::handleTickEvent()
{
	// My implementation of HAL_UART_Receive() function
	if (huart7.RxState == HAL_UART_STATE_READY)
	{		
		/* Process Locked */
    do{                                        \
			if(huart7.Lock == HAL_LOCKED)   \
			{                                      \
				 return;		                  \
			}                                      \
			else                                   \
			{                                      \
				 huart7.Lock = HAL_LOCKED;    \
			}                                      \
		} while (0U);
    
    huart7.ErrorCode = HAL_UART_ERROR_NONE;
    huart7.RxState = HAL_UART_STATE_BUSY_RX;
		
		static bool inited = false;
		static char buffer[BUFFER_SIZE];
		static uint32_t i = 0;
		
		if (!inited)
		{
			memset(buffer, '\0', BUFFER_SIZE);
		}
		
		/* Init tickstart for timeout managment */
    uint32_t tickstart = HAL_GetTick();
		uint32_t timeout = 50;
		
		while (UART_WaitOnFlagUntilTimeout(&huart7, UART_FLAG_RXNE, RESET, tickstart, timeout) == HAL_OK)
		{
			buffer[i] = (uint8_t)(huart7.Instance->DR & (uint8_t)0x00FF);
			inited = true;
			
			if (strstr(buffer, "\n") != NULL)
			{
				char* charPtr = strstr(buffer, "MSSG$demo$");
				if (charPtr != NULL)
				{
					charPtr += strlen("MSSG$demo$");
					
					for (int i = scrollList1ListItems.getNumberOfDrawables() - 1; i > 0; --i)
					{
						scrollList1ListItems.element[i].SetText(scrollList1ListItems.element[i - 1].GetText());
					}
					scrollList1ListItems.element[0].SetText(charPtr);
					
					scrollList1.invalidate();
				}
				
				memset(buffer, '\0', BUFFER_SIZE);
				i = 0;
				inited = false;
			}
			else if (i > BUFFER_SIZE -1)
			{
				i = 0;
				memset(buffer, '\0', BUFFER_SIZE);
				inited = false;
			}
			else
			{
				++i;
			}
		}
		
		/* At end of Rx process, restore huart->RxState to Ready */
    huart7.RxState = HAL_UART_STATE_READY;
		
		do {                                       \
			huart7.Lock = HAL_UNLOCKED;    \
		} while (0U);
	}
}

